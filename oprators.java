public class oprators {
    public static void main(String [] args){
        //relational operator  <   >   <=  >=   ==  !=
        //logical operator    && || !
        int a = 10;
        int b = 20;
        boolean x = true;
        boolean y = false;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(x&&y);
        System.out.println(x||y);
        System.out.println(!x);
    }
}
